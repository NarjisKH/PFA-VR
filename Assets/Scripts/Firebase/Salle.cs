
public class Salle
{
    public string titre;
    public string matiere;
    public string description;
    public string scene;

    public Salle() {
    }
    public Salle(string titre, string matiere,string description,string scene) {
        this.titre = titre;
        this.matiere = matiere;
        this.description = description;
        this.scene = scene;
}
}
